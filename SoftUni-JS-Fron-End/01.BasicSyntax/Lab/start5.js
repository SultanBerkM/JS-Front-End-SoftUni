function mathOperations(num1, num2, operator){
    if(operator === '+'){ console.log(num1+num2)}
    if(operator === '-'){console.log(num1-num2)}
    if(operator === '*'){console.log(num1*num2)}
    if(operator === '/'){console.log(num1/num2)}
    if(operator === '%'){console.log(num1%num2)}
    if(operator === '**'){console.log(num1**num2)}
}
mathOperations(12,2,'**')